// NOTE: Try to silently detect cmpp in specified port, but will try all possible to not give a false negative.
//       If port is bad behaved (ie: cannot write or close normal) this function will fail for safe avoiding false negative mismatch.
//       In other words: If is says the cmpp is not in that port, it is certainly not there at that particular moment.
export const transactWithCmpp = async (portOpened: PortOpened, frames: readonly ([frame:readonly number[],txInterval: number])[], numOfResponseFramesExpected: number, consumer: (_:FrameInterpreted, framesSent: number) => void): Promise<void> => {
    let framesSent = 0
    log.text = 'first line of `transactWithCmpp` function'
    return new Promise<void>(async (resolve, reject) => {
        let hasError = false // when an error occurs this flag is set to true to signal that no more work must be done
        let rxCounter = numOfResponseFramesExpected //frames.length
        let rxBuf: number[] = []
        log.text = 'setting port reception handler'
        portOpened.onData(data => { 
            rxBuf = [...rxBuf, ...data] 
            const interpreter = InterpretIncomming(ESC,[STX,ACK,NACK],ETX)(
                //onFinished
                (frameInterpreted, rawInput) => {
                    log.text = `onFrameInterpreted ${frameInterpreted}`
                    rxCounter = rxCounter - 1
                    consumer(frameInterpreted, framesSent)
                    if(rxCounter===0) {
                        resolve(undefined) //finish transmiting all packages and its sucessfull
                    }
                },
                //onError
                (errorMessage, partialFrame, rawInput) => {
                    log.text = `onInterpretationError`
                    hasError = true
                    reject({
                        kind: `Error interpreting cmppFrame`,
                        errorMessage,
                        partialFrame,
                        rawInput,
                    })
                },
                //onInternalStateChange
                (currentState, partialFrame, waitingEscDup, rawInput) => {
                    log.text = `onStateChange: (partial frame) ${partialFrame}`
                }
            )
            log.text = `Runing interpreter on received data`
            data.map(interpreter)
        })
        //log.text = 'configuring writing to port'
        const writeAllTx = frames.map( frameAndInterval => {
            const [txFrame, txInterval] = frameAndInterval
            return () => new Promise<void>( (resolve__, reject__) => {
                if(hasError===false) {
                    //log.text = `writing to the port frame number ${c++}` 
                    framesSent++
                    portOpened.write(txFrame)
                    .then(() => {
                        //log.text = 'has finished to write to the port, starting to count txInterval' 
                       
                    })
                    // port is bad bahaved
                    .catch(err => {
                        //log.text = `error on write tx to the port`
                        hasError = true
                        reject__(`Error on write data to an alredy opened serial port. Tried to write frame '${txFrame}' but got an run-time error, error details: "${String(err)}"`)
                    })
                    setTimeout(() => {
                        //log.text = `TxInterval has been reached` 
                        resolve__(undefined)
                    }, txInterval)
                } else {
                    resolve__(undefined) // fix: I'm not sure if would call resolve or reject in this condition. Check in future
                }
                
                
            })
        })

        log.text = 'Start to writing to the port'
        await executeInSequence(writeAllTx)
        
    })

}



